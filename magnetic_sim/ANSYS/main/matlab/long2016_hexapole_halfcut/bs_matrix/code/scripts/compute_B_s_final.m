%% compute_B_s_final.m â€” Final B_S matrix per user's convention
%
%  Steps:
%   (1) Start from B_matrix_at_Iin (FEM raw, Tesla)
%   (2) Flip upper coil columns (P2, P4, P5) â€” corrects ANSYS sign asymmetry
%   (3) Multiply by 130e-3 / I_in   â†’ B_S in V/A
%   (4) Flip lower pole rows (P1, P3, P6) Ă— -1   â†’ user sign convention
%   (5) Output LaTeX

clear; clc;

S = load('G:\my_workspace\code\FEM_sim\magnetic_sim\ANSYS\main\matlab\long2016_hexapole_halfcut\bs_matrix\data\B_bar.mat');
B = S.B_matrix_at_Iin;          % Tesla
I_in   = S.I_in_A;              % 0.6 A
S_hall = S.S_hall;              % 0.13 V/T
coef   = S_hall / I_in;         % 0.2167 V/(TÂ·A)

% Step 2: flip upper coil columns
upper_cols = [2 4 5];
B_corr = B;
B_corr(:, upper_cols) = -B_corr(:, upper_cols);

% Step 3: multiply by coefficient â†’ B_S in V/A
B_S = coef * B_corr;

% Step 4: flip only the lower-lower off-diagonal entries (6 entries)
% so that ALL off-diag become negative while diag stay positive
lower = [1 3 6];
for ii = lower
    for jj = lower
        if ii ~= jj
            B_S(ii, jj) = -B_S(ii, jj);
        end
    end
end

%% Display
fprintf('B_S matrix [mV/A]:\n');
fprintf('     P1          P2          P3          P4          P5          P6\n');
for i = 1:6
    fprintf('P%d  ', i);
    for j = 1:6
        fprintf(' %+11.4f', B_S(i,j)*1e3);
    end
    fprintf('\n');
end

fprintf('\nDiagonal:\n');
for i = 1:6
    layer = 'upper'; if ismember(i, [1 3 6]); layer = 'lower'; end
    fprintf('  P%d (%s)  = %+8.4f mV/A\n', i, layer, B_S(i,i)*1e3);
end

