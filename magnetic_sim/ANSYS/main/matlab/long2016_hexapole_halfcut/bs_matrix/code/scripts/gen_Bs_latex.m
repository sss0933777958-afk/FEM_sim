fprintf(fid, '\\usepackage{amsmath}\n');
fprintf(fid, '\\begin{document}\n\n');
fprintf(fid, '\\[\nB_S = \\begin{bmatrix}\n');
for i = 1:6
    for j = 1:6
        fprintf(fid, '%+9.4f', B_S(i,j));
        if j < 6, fprintf(fid, ' & '); end
    end
    fprintf(fid, ' \\\\\n');
end
fprintf(fid, '\\end{bmatrix}\\ \\mathrm{V/A}\n\\]\n\n');
fprintf(fid, ['\\noindent $B_S = (S_{hall}/I_{in})\\,\\bar{B}_S$,\\ ' ...
              'with $S_{hall} = 130$\\,V/T and $I_{in} = %.2f$\\,A.\\\\\n'], I_in);
fprintf(fid, ['Row $i$ = $B_{\\mathrm{surface}}$ sensor on pole $i$;\\ ' ...
              'column $j$ = excited pole $j$;\\ order P1--P6.\\\\\n']);
fprintf(fid, ['Sign convention: diagonal $+|\\cdot|$, off-diagonal ' ...
              '$-|\\cdot|$ (magnitudes are $|(S_{hall}/I_{in})\\,\\bar{B}_S|$).\n']);
fprintf(fid, '\n\\end{document}\n');

fprintf('Saved LaTeX: %s\n', tex_file);
