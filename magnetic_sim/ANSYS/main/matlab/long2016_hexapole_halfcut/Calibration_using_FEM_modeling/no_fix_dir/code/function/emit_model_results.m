function emit_model_results()
% EMIT_MODEL_RESULTS  no_fix_l（18-param bias 電荷模型）結果，Hall 表格式一檔輸出
%   K̄_I / ℓ̂ / G / F / ^Bĝ_I / ê。純結果（無 caption）、單位標題（無因次/10^0 不標）。
%   獨立於 main.m（自足載 fit_bias_*.mat 出 PDF）。
%   F = 1A × I₆（paper P1..P6）；G = ^Bĝ_I·K̄·F（mT）；ê 由 e_hat(17) 依 e6z 約束重建。
%   輸出：no_fix_dir/results/model_results_gap200um_mueq.pdf。

    VARIANT = 'gap200um_mueq';
    here   = fileparts(mfilename('fullpath'));
    base   = fileparts(fileparts(here));                  % .../no_fix_dir
    out_dir = fullfile(base,'results');
    if ~exist(out_dir,'dir'); mkdir(out_dir); end

    S = load(fullfile(base,'data',sprintf('fit_bias_R150um_%s.mat',VARIANT)));   % [MODIFIED] 全載（含 σ_tot/iso_tot）
    coil_sign = [1 -1 1 -1 -1 1];               % all-source 顯示翻號（欄縮放翻上極 P2/P4/P5；同 write_KbarI_tex）
    K_bar    = S.Khat .* coil_sign;             % K̄_I（無因次，K̄(1,1)=5/6）→ 對角全正、off-diag 全負
    ell      = S.ell;       % ℓ̂ [µm]
    ghat_I_B = S.gB;        % ^Bĝ_I [mT/A]
    hasAiso  = isfield(S,'sigma_tot') && isfield(S,'iso_tot');   % 控制範圍總代表值
    I_A = 1;                % 激發電流 [A]
    F = I_A * eye(6);       % 電流矩陣 [A]
    G = ghat_I_B * K_bar * F;   % [mT]（用翻號後 K̄ → G 上極對角也轉正）
    e = S.e_hat;                                   % 17-vec bias
    E36 = zeros(3,6);                              % 重建 ê(3×6)（含 e6z 約束）
    E36(:,1)=e(1:3); E36(:,2)=e(4:6); E36(:,3)=e(7:9); E36(:,4)=e(10:12); E36(:,5)=e(13:15);
    E36(1,6)=e(16);  E36(2,6)=e(17);  E36(3,6)=e(1)-e(4)+e(8)-e(11)+e(15);

    fprintf('ℓ̂=%.1f µm, ^Bĝ_I=%.4e mT/A\n', ell, ghat_I_B);

    pole = {'P1','P2','P3','P4','P5','P6'};
    tex_path = fullfile(out_dir, sprintf('model_results_%s.tex', VARIANT));
    pdf_path = fullfile(out_dir, sprintf('model_results_%s.pdf', VARIANT));
    fid = fopen(tex_path,'w');
    fprintf(fid,'%% Auto-generated by emit_model_results.m\n');
    fprintf(fid,'\\documentclass[11pt]{article}\n\\usepackage[margin=1in]{geometry}\n\\usepackage{amsmath}\n\\begin{document}\n');
    emit_mat(fid, '\bar{K}_{I}', K_bar, pole, '');          % 無因次 → 不標單位
    fprintf(fid,'\\[\n\\hat{\\ell} = %.1f~\\mu\\mathrm{m}\n\\]\n', ell);
    emit_mat(fid, 'G~[\mathrm{mT}]', G, pole, '');
    emit_mat(fid, 'F~[\mathrm{A}]', F, pole, '');
    emit_scalar_unit(fid, '{}^{B}\hat{g}_{I}', ghat_I_B, 'mT/A');   % 10^0 不標
    emit_e(fid, '\hat{e}', E36, pole, '');                  % 3×6 bias，無因次 → 不標單位
    if hasAiso   % 控制範圍總代表值：σ_tot=總增益(有單位)、iso_tot=σmax/σmin(無因次不標)
        fprintf(fid,'\\[\n \\sigma_{tot} = %.4f~\\mathrm{mT/A}\n\\]\n', S.sigma_tot);
        fprintf(fid,'\\[\n \\mathrm{iso}_{tot} = %.4f\n\\]\n', S.iso_tot);
        if isfield(S,'Np_range')
            fprintf(fid,'\\noindent\\small $\\sigma_{tot}=\\overline{\\|T\\|_F}$, $\\mathrm{iso}_{tot}=\\overline{\\sigma_{\\max}/\\sigma_{\\min}}$ over %d real nodes in $R\\le150~\\mu$m ball.\n', S.Np_range);
        end
    end
    fprintf(fid,'\\end{document}\n');
    fclose(fid);

    xelatex = 'C:\Users\Kuo\AppData\Local\Programs\MiKTeX\miktex\bin\x64\xelatex.exe';
    old = cd(out_dir);
    [st,so] = system(sprintf('"%s" -interaction=nonstopmode -halt-on-error "%s"', xelatex, tex_path));
    cd(old);
    if st~=0 || ~exist(pdf_path,'file'); fprintf('%s\n',so); error('xelatex 編譯失敗'); end
    for e2 = {'.tex','.aux','.log','.out'}, f = fullfile(out_dir, sprintf('model_results_%s%s', VARIANT, e2{1})); if exist(f,'file'); delete(f); end, end
    fprintf('已存 %s\n', pdf_path);
end

function emit_mat(fid, name, M, lab, fmode)
    Ms = M; fac = '';
    if strcmp(fmode,'auto'), mx=max(abs(M(:))); if mx>0, e=floor(log10(mx)); if e~=0, Ms=M/10^e; fac=sprintf('10^{%d}\\,',e); end, end, end
    fprintf(fid,'\\[\n%s = %s\\begin{array}{c|cccccc}\n ', name, fac);
    for j=1:6, fprintf(fid,'& %s ', lab{j}); end
    fprintf(fid,'\\\\\\hline\n');
    for i=1:6, fprintf(fid,'%s ', lab{i}); for j=1:6, fprintf(fid,'& %+9.4f ', Ms(i,j)); end, fprintf(fid,'\\\\\n'); end
    fprintf(fid,'\\end{array}\n\\]\n');
end

function emit_e(fid, name, E, collab, fmode)
    Es = E; fac = '';
    if strcmp(fmode,'auto'), mx=max(abs(E(:))); if mx>0, e=floor(log10(mx)); if e~=0, Es=E/10^e; fac=sprintf('10^{%d}\\,',e); end, end, end
    erow = {'e_x','e_y','e_z'};
    fprintf(fid,'\\[\n%s = %s\\begin{array}{c|cccccc}\n ', name, fac);
    for j=1:6, fprintf(fid,'& %s ', collab{j}); end
    fprintf(fid,'\\\\\\hline\n');
    for i=1:3, fprintf(fid,'%s ', erow{i}); for j=1:6, fprintf(fid,'& %+9.4f ', Es(i,j)); end, fprintf(fid,'\\\\\n'); end
    fprintf(fid,'\\end{array}\n\\]\n');
end

function emit_scalar_unit(fid, name, val, unit)
    ge = floor(log10(abs(val)));
    if ge==0
        fprintf(fid,'\\[\n %s = %.4f~\\mathrm{%s}\n\\]\n', name, val, unit);
    else
        fprintf(fid,'\\[\n %s = %.3f\\times10^{%d}~\\mathrm{%s}\n\\]\n', name, val/10^ge, ge, unit);
    end
end
