function emit_model_results()
% EMIT_MODEL_RESULTS  Hall-sensor (single-parameter) 校正結果，Hall 表格式一檔輸出
%   D̄ / G / V / ℓ̂ / ^Bĝ_V。純結果（無 caption）、單位標題（無因次/10^0 不標，per figure-style.md）。
%   獨立於 main.m（自足載 calib_D_*.mat 出 PDF）。
%   輸出：Hall_sensor_base_fix_dir/results/model_results_gap200um_mueq.pdf。

    VARIANT = 'gap200um_mueq';
    here   = fileparts(mfilename('fullpath'));            % .../code/function
    base   = fileparts(fileparts(here));                  % .../Hall_sensor_base_fix_dir
    out_dir = fullfile(base,'results');
    if ~exist(out_dir,'dir'); mkdir(out_dir); end

    S = load(fullfile(base,'data',sprintf('calib_D_%s.mat',VARIANT)), ...
             'D_bar','Dv_p','Vmat_p','ell_hat','g_V');
    D_bar    = S.D_bar;      % 無因次（D̄(1,1)=5/6）
    G        = S.Dv_p;       % [mT]
    V        = S.Vmat_p;     % [mV]
    ell_hat  = S.ell_hat;    % [µm]
    ghat_V_B = S.g_V;        % [mT/mV]

    fprintf('ℓ̂=%.1f µm, ^Bĝ_V=%.4e mT/mV\n', ell_hat, ghat_V_B);

    pole = {'P1','P2','P3','P4','P5','P6'};
    tex_path = fullfile(out_dir, sprintf('model_results_%s.tex', VARIANT));
    pdf_path = fullfile(out_dir, sprintf('model_results_%s.pdf', VARIANT));
    fid = fopen(tex_path,'w');
    fprintf(fid,'%% Auto-generated by emit_model_results.m\n');
    fprintf(fid,'\\documentclass[11pt]{article}\n\\usepackage[margin=1in]{geometry}\n\\usepackage{amsmath}\n\\begin{document}\n');
    emit_mat(fid, '\bar{D}', D_bar, pole, '');              % 無因次 → 不標單位
    emit_mat(fid, 'G~[\mathrm{mT}]', G, pole, '');
    emit_mat(fid, 'V~[\mathrm{mV}]', V, pole, '');
    fprintf(fid,'\\[\n\\hat{\\ell} = %.1f~\\mu\\mathrm{m}\n\\]\n', ell_hat);
    emit_scalar_unit(fid, '{}^{B}\hat{g}_{V}', ghat_V_B, 'mT/mV');   % 10^0 不標
    fprintf(fid,'\\end{document}\n');
    fclose(fid);

    xelatex = 'C:\Users\Kuo\AppData\Local\Programs\MiKTeX\miktex\bin\x64\xelatex.exe';
    old = cd(out_dir);
    [st,so] = system(sprintf('"%s" -interaction=nonstopmode -halt-on-error "%s"', xelatex, tex_path));
    cd(old);
    if st~=0 || ~exist(pdf_path,'file'); fprintf('%s\n',so); error('xelatex 編譯失敗'); end
    for e = {'.tex','.aux','.log','.out'}, f = fullfile(out_dir, sprintf('model_results_%s%s', VARIANT, e{1})); if exist(f,'file'); delete(f); end, end
    fprintf('已存 %s\n', pdf_path);
end

%% ---- local：Hall 表格式 6×6（無 caption；單位標在 name）----
function emit_mat(fid, name, M, lab, fmode)
    Ms = M; fac = '';
    if strcmp(fmode,'auto'), mx=max(abs(M(:))); if mx>0, e=floor(log10(mx)); if e~=0, Ms=M/10^e; fac=sprintf('10^{%d}\\,',e); end, end, end
    fprintf(fid,'\\[\n%s = %s\\begin{array}{c|cccccc}\n ', name, fac);
    for j=1:6, fprintf(fid,'& %s ', lab{j}); end
    fprintf(fid,'\\\\\\hline\n');
    for i=1:6, fprintf(fid,'%s ', lab{i}); for j=1:6, fprintf(fid,'& %+9.4f ', Ms(i,j)); end, fprintf(fid,'\\\\\n'); end
    fprintf(fid,'\\end{array}\n\\]\n');
end

%% ---- local：純量 + 單位（10^0 不標）----
function emit_scalar_unit(fid, name, val, unit)
    ge = floor(log10(abs(val)));
    if ge==0
        fprintf(fid,'\\[\n %s = %.4f~\\mathrm{%s}\n\\]\n', name, val, unit);
    else
        fprintf(fid,'\\[\n %s = %.3f\\times10^{%d}~\\mathrm{%s}\n\\]\n', name, val/10^ge, ge, unit);
    end
end
