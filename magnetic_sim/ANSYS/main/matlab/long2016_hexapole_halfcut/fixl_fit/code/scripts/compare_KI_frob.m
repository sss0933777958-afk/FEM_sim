%% compare_KI_frob.m
%  量化 K̂_I(R=150um) vs K̂_I(R=500um) 的相似度（相對 Frobenius）。
%  兩矩陣同 gauge（k̂11=5/6、all-source、欄=激發極/列=電荷極），直接逐元素比。
%  矩陣取自 fit_KI_R150.tex / fit_KI_R500.tex（all-source 顯示版，4 位小數）。
clear; clc;

% R = 150 um
A = [ 0.8333 -0.0822 -0.1857 -0.1721 -0.1751 -0.2005;
     -0.1947  0.9782 -0.2254 -0.1460 -0.1476 -0.2353;
     -0.1896 -0.1707  0.8318 -0.0801 -0.1712 -0.2002;
     -0.2226 -0.1476 -0.1911  0.9800 -0.1490 -0.2321;
     -0.2247 -0.1474 -0.2214 -0.1463  0.9818 -0.2048;
     -0.1892 -0.1724 -0.1860 -0.1714 -0.0839  0.8311];

% R = 500 um
B = [ 0.8333 -0.1427 -0.1555 -0.1812 -0.1846 -0.1551;
     -0.2060  0.9934 -0.2321 -0.1527 -0.1566 -0.2289;
     -0.1580 -0.1795  0.8326 -0.1419 -0.1814 -0.1556;
     -0.2274 -0.1540 -0.2039  0.9944 -0.1580 -0.2253;
     -0.2282 -0.1542 -0.2263 -0.1540  0.9934 -0.2054;
     -0.1573 -0.1806 -0.1563 -0.1801 -0.1480  0.8462];

relFrob = norm(A-B,'fro')/norm(A,'fro')*100;     % 主指標
absFrob = norm(A-B,'fro');
D = abs(A-B);
[mx,li] = max(D(:)); [ri,ci] = ind2sub(size(D),li);
colrel = vecnorm(A-B)./vecnorm(A)*100;           % 逐欄（激發極）相對差

fprintf('==== K_I(R150) vs K_I(R500) similarity ====\n');
fprintf('relative Frobenius  ||A-B||_F / ||A||_F = %.4f %%\n', relFrob);
fprintf('absolute Frobenius  ||A-B||_F           = %.4f\n', absFrob);
fprintf('max |A-B|                               = %.4f  @ (P%d,P%d)\n', mx, ri, ci);
fprintf('per-excited-pole rel diff [%%]: P1=%.2f P2=%.2f P3=%.2f P4=%.2f P5=%.2f P6=%.2f\n', colrel);
